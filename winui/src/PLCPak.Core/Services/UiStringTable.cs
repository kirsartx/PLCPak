    public static string Get(string key, string language)
    {
        if (!Strings.TryGetValue(key, out var pair))
            return key;

        return NormalizeLanguage(language) == "en" ? pair.En : pair.Zh;
    }

    public static string NormalizeLanguage(string? language)
        => string.Equals(language, "en", StringComparison.OrdinalIgnoreCase) ? "en" : "zh";

    public static string NormalizeTheme(string? theme)
        => string.Equals(theme, "dark", StringComparison.OrdinalIgnoreCase) ? "dark" : "light";

    public static string GetNavLabel(string page, string language)
    {
        var normalized = page.Trim().ToLowerInvariant();
        var key = normalized switch
        {
            "quick" or "jobs" or "dashboard" or "wizard" or "operations" => $"nav.{normalized}",
            _ => "nav.dashboard"
        };
        return Get(key, language);
    }
